#define INST_ADDR_START 0
#define USRSYS_STACK_START 0x00100000
#define SVC_STACK_START 0x00300000
#define IRQ_STACK_START 0x00400000
#define TASK_STACK_START 0x00500000
#define GLOBAL_ADDR_START 0x04500000
#define DALLOC_ADDR_START 0x04600000

#define INST_MEM_SIZE (USRSYS_STACK_START - INST_ADDR_START)     //0x100000
#define USRSYS_STACK_SIZE (SVC_STACK_START - USRSYS_STACK_START) //0x200000
#define SVC_STACK_SIZE (IRQ_STACK_START - SVC_STACK_START)       //0x100000
#define IRQ_STACK_SIZE (TASK_STACK_START - IRQ_STACK_START)      //0x100000
#define TASK_STACK_SIZE (GLOBAL_ADDR_START - TASK_STACK_START)   //0x4000000

#define DALLOC_MEM_SIZE (55*1024*1024)

#define USRSYS_STACK_TOP (USRSYS_STACK_START+USRSYS_STACK_SIZE - 4) //0x2FFFFC
#define SVC_STACK_TOP (SVC_STACK_START+SVC_STACK_SIZE - 4) //0x3FFFFC
#define IRQ_STACK_TOP (IRQ_STACK_START+IRQ_STACK_SIZE - 4) //0x4FFFFC